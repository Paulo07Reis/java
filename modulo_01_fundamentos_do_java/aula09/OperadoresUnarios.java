package modulo_01_fundamentos_do_java.aula09;

public class OperadoresUnarios {
    public static void main(String[] args) {
        int n = 5;
        System.out.println(- n);
        System.out.println(n);
        n++;
        System.out.println(n);
        System.out.println(n++);
        System.out.println(n);

        boolean falsa = false;
        System.out.println(!falsa);
        System.out.println(falsa);
    }
}
