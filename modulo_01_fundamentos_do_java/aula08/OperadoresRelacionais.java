package modulo_01_fundamentos_do_java.aula08;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int n1 = 1;
        int n2 = 2;

        if(n1 > n2){
            System.out.println("N1 > N2");
        }

        if (n1 < n2){
            System.out.println("N1 < N2");
        }

        if (n1 != n2){
            System.out.println("N1 != N2");
        }
    }
}
